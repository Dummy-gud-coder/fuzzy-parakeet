const CACHE_NAME = 'favicon-cache-v1';

self.addEventListener('install', (event) => {
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  event.waitUntil(self.clients.claim());
});

self.addEventListener('fetch', (event) => {
  const url = new URL(event.request.url);

  // Intercept requests directed to DuckDuckGo's icon endpoint
  if (url.hostname === 'icons.duckduckgo.com') {
    event.respondWith(
      caches.open(CACHE_NAME).then((cache) => {
        return cache.match(event.request).then((cachedResponse) => {
          // Return the icon immediately if it exists in the Service Worker cache
          if (cachedResponse) {
            return cachedResponse;
          }

          // Fetch fresh if not cached, clone the response, and store it
          return fetch(event.request).then((networkResponse) => {
            if (networkResponse.status === 200) {
              cache.put(event.request, networkResponse.clone());
            }
            return networkResponse;
          });
        });
      })
    );
  }
});
